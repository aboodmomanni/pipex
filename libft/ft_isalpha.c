#include "libft.h"

int	ft_isalpha(int i)
{
	if (i >= 'a' && i <= 'z')
	{
		return (1);
	}
	else if (i >= 'A' && i <= 'Z')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
